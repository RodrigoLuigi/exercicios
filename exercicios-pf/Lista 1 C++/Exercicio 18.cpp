/*18) Fazer um algoritmo que imprima os números de 1 a 100 e depois de 100 até 1.*/



#include <iostream>
using namespace std;
int main(){
        int i,ii;
        for (i=1 ; i<=100 ; i++){
            cout << "\n" << i;
        }
        for (ii=100 ; ii>=1 ; ii--){
            cout << "\n" << ii;
        }
}

